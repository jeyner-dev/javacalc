public class SimpleCalculator {
    public static float add(int x, int y) {
        return x + y;
    }
    public static float sub(int x, int y) {
        return x - y;
    }
    public static float div(int x, int y) {
        return (float)x / y;
    }
    public static float mul(int x, int y) {
        return x * y;
    }
}